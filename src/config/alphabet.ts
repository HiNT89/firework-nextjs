// Helper function to get text pattern points
export function getTextPoints(
  text: string,
  scale: number = 1,
  centerX: number = 0,
  centerY: number = 0,
): { x: number; y: number }[] {
  const points: { x: number; y: number }[] = [];
  const upperText = text.toUpperCase();

  // Calculate total width
  let totalWidth = 0;
  for (let i = 0; i < upperText.length; i++) {
    totalWidth += LETTER_WIDTH + LETTER_SPACING;
  }
  totalWidth -= LETTER_SPACING; // Remove last spacing

  // Starting position (centered)
  let currentX = -totalWidth / 2;

  for (let i = 0; i < upperText.length; i++) {
    const char = upperText[i];
    const letterPoints = ALPHABET[char];

    if (letterPoints) {
      letterPoints.forEach(([px, py]) => {
        // Center the letter vertically (LETTER_HEIGHT / 2)
        const x = (currentX + px) * scale + centerX;
        const y = (py - LETTER_HEIGHT / 2) * scale + centerY;
        points.push({ x, y });
      });
    }

    currentX += LETTER_WIDTH + LETTER_SPACING;
  }

  return points;
}

export default ALPHABET;
